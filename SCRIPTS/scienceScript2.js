function hideFundemental() {
    if (fundemental.style.display == "none")
    {
        fundemental.style.display = "block";
        fundemental2.style.display = "block";
    }
    else
    {
        fundemental.style.display = "none";
        fundemental2.style.display = "none";
    }
}

