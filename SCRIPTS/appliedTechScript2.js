

function hideBusinessCourses() {
    if (businessEducation.style.display == "none")
    {
        businessEducation.style.display = "block";
        businessEducation2.style.display = "block";
    }
    else
    {
        businessEducation.style.display = "none";
        businessEducation2.style.display = "none";
    }
}

